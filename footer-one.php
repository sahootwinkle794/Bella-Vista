  btn.addEventListener("click", () => {
    let type = btn.dataset.type;
    let count = document.getElementById(type + "-count");
    if (parseInt(count.textContent) > 0) {
      count.textContent = parseInt(count.textContent) - 1;
      updateTotal();
    }
  });
});

